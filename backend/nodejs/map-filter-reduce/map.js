/* map()
  - Executa uma função em todos os itens de um array e retorna um novo array após a manipulação.
*/

const frutas = ['Apple', 'Grape', 'Lemon', 'Strawberry', 'Papaya'];
const frutasMapeadas = frutas.map(fruta => 'Fruta: ' + fruta);

console.log(frutas);
console.log(frutasMapeadas);
console.log(frutas);